package com.sforce.dataset.flow.node;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
//@JsonIdentityInfo(generator=ObjectIdGenerators.IntSequenceGenerator.class, property="@id")
public class FilterExpression {
	public String field = null;
	public String operator = null;
	public String value = null;
	public boolean isValueQuoted = false;
	public FilterCondition subCondition = null;
	
	@JsonIgnore
	public static FilterExpression getFilterExpression(String field,String operator, String value, boolean isValueQuoted, FilterCondition subCondition)
	{
		FilterExpression node = new FilterExpression();
		node.field = field;
		node.operator = operator;
		node.value = value;
		node.isValueQuoted = isValueQuoted;
		node.subCondition = subCondition;
		return node;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((field == null) ? 0 : field.hashCode());
		result = prime * result + (isValueQuoted ? 1231 : 1237);
		result = prime * result
				+ ((operator == null) ? 0 : operator.hashCode());
		result = prime * result
				+ ((subCondition == null) ? 0 : subCondition.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		FilterExpression other = (FilterExpression) obj;
		if (field == null) {
			if (other.field != null) {
				return false;
			}
		} else if (!field.equals(other.field)) {
			return false;
		}
		if (isValueQuoted != other.isValueQuoted) {
			return false;
		}
		if (operator == null) {
			if (other.operator != null) {
				return false;
			}
		} else if (!operator.equals(other.operator)) {
			return false;
		}
		if (subCondition == null) {
			if (other.subCondition != null) {
				return false;
			}
		} else if (!subCondition.equals(other.subCondition)) {
			return false;
		}
		if (value == null) {
			if (other.value != null) {
				return false;
			}
		} else if (!value.equals(other.value)) {
			return false;
		}
		return true;
	}
	
	

}
